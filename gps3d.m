function p = gps3d(a, b, c, d, ra, rb, rc, rd)


   A= [2*(a(1)-b(1)) 2*(a(2)-b(2)) 2*(a(3)-b(3)) rb^2-ra^2+a(1)^2+a(2)^2+a(3)^2-b(1)^2-b(2)^2-b(3)^2] ;
   B= [2*(b(1)-c(1)) 2*(b(2)-c(2)) 2*(b(3)-c(3)) rc^2-rb^2+b(1)^2+b(2)^2+b(3)^2-c(1)^2-c(2)^2-c(3)^2] ;
   C= [2*(c(1)-d(1)) 2*(c(2)-d(2)) 2*(c(3)-d(3)) rd^2-rc^2+c(1)^2+c(2)^2+c(3)^2-d(1)^2-d(2)^2-d(3)^2] ;
   
    x =my_rref([A;B;C]);
    y = size(x);
    
    p=x(:,y(2));
    disp(p);
    
end